export const generateSeats = (busId: string): Seat[] => {
  const seats: Seat[] = [];
  const bookedSeats = ["1A", "1B", "3C", "5D", "6A", "8B"];
  const cashReservedSeats = ["2C", "4A", "7D"];
  
  for (let row = 1; row <= 10; row++) {
    for (let col = 0; col < 4; col++) {
      const columns = ["A", "B", "C", "D"];
      const seatNumber = `${row}${columns[col]}`;
      const isPremium = row <= 2;
      
      let status: 'available' | 'booked' | 'cash_reserved' = 'available';
      if (bookedSeats.includes(seatNumber)) {
        status = 'booked';
      } else if (cashReservedSeats.includes(seatNumber)) {
        status = 'cash_reserved';
      }
      
      seats.push({
        id: `${busId}-${seatNumber}`,
        number: seatNumber,
        row,
        column: col,
        status,
        type: isPremium ? "premium" : "regular",
      });
    }
  }
  
  return seats;
};

